const TRANSLATIONS: Record<Lang, Dict> = { es: ES, en: EN };

type Ctx = {
  lang: Lang;
  setLang: (l: Lang) => void;
};

const LangContext = createContext<Ctx | null>(null);

const STORAGE_KEY = "timbre_lang";

export function LangProvider({ children }: { children: React.ReactNode }) {
  const [lang, setLangState] = useState<Lang>("es");

  useEffect(() => {
    if (typeof window === "undefined") return;
    const stored = window.localStorage.getItem(STORAGE_KEY);
    if (stored === "es" || stored === "en") setLangState(stored);
  }, []);

  function setLang(l: Lang) {
    setLangState(l);
    if (typeof window !== "undefined") {
      window.localStorage.setItem(STORAGE_KEY, l);
    }
  }

  return <LangContext.Provider value={{ lang, setLang }}>{children}</LangContext.Provider>;
}

export function useLang(): Ctx {
  const ctx = useContext(LangContext);
  if (!ctx) throw new Error("useLang must be inside LangProvider");
  return ctx;
}

// Hook que devuelve la función de traducción. `t(key)` busca la traducción;
// si no existe en el idioma activo, hace fallback a ES; si tampoco está,
// devuelve la propia key — así el dev ve qué falta traducir.
//
// Soporta interpolación simple {var}: t("portal.stat.queued", {n: 5}).
export function useT() {
  const { lang } = useLang();
  return (key: string, vars?: Record<string, string | number>): string => {
    const raw = TRANSLATIONS[lang][key] ?? TRANSLATIONS.es[key] ?? key;
    if (!vars) return raw;
    return raw.replace(/\{(\w+)\}/g, (_, name) => String(vars[name] ?? `{${name}}`));
  };
}

// useStatusLabel: igual que statusLabel() de lib/api.ts pero respetando el
// idioma activo. Si el status no está mapeado, devuelve la string cruda.
export function useStatusLabel() {
  const t = useT();
  return (status: string): string => {
    if (!status) return "—";
    const key = `status.${status}`;
    const translated = t(key);
    return translated === key ? status : translated;
  };
}
